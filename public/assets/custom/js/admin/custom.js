var click = 1;
$('.show_description_sub_inputs').on('click', function(){

var id = $(this).attr('id');
var lastChar = id.substr(id.length - 1);
var lastLastChar = id.substr(id.length - 2);

var firstChar = id.charAt(0);
if(Math.floor(lastLastChar) == lastLastChar && $.isNumeric(lastLastChar)) {
	lastChar = lastLastChar;
}

if(click %2 == 0) {
	$('.' + firstChar + '_description_' + lastChar + '_sub_inputs').addClass('hide');
} else {
	$('.' + firstChar + '_description_' + lastChar + '_sub_inputs').removeClass('hide');
}
	click++;
})
